//criando função declarativa
function funcao() {
    console.log("Sou uma mensagem de uma função declarativa!");
}
funcao();

//expressão de função
//com nomeação
var funcao2 = function funcao2() {
    console.log("Sou uma mensagem de uma função expressão!");
}

funcao2();

//arrow function
var funcao3 = () =>{
    console.log("Sou uma mensagem de uma arrow function!");
}

funcao3();